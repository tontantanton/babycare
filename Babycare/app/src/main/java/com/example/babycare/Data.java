public class Data {
    private String name;
    private String calories;
    private String protein;
    private String vitamina;
    private String vitaminc;
    private String vitaminb6;
    private String vitaminb12;
    private String calcium;
    private String iodine;
    private String iron;





    public Data(String name, String calories, String protein, String vitamina, String vitaminc, String vitaminb6, String vitaminb12
            ,String calcium, String iodine, String iron) {
        this.name = name;
        this.calories = calories;
        this.protein = protein;
        this.vitamina = vitamina;
        this.vitaminc = vitaminc;
        this.vitaminb6 = vitaminb6;
        this.vitaminb12 = vitaminb12;
        this.calcium = calcium;
        this.iodine = iodine;
        this.iron = iron;

    }





    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCalories() {
        return calories;
    }

    public void setCalories(String calories) {
        this.calories = calories;
    }

    public String getProtein() {
        return protein;
    }

    public void setProtein(String protein) {
        this.protein = protein;
    }

    public String getVitamina() {
        return vitamina;
    }

    public void setVitamina(String vitamina) {
        this.vitamina = vitamina;
    }

    public String getVitaminc() {
        return vitaminc;
    }

    public void setVitaminc(String vitaminc) {
        this.vitaminc = vitaminc;
    }

    public String getVitaminb6() {
        return vitaminb6;
    }

    public void setVitaminb6(String vitaminb6) {
        this.vitaminb6 = vitaminb6;
    }

    public String getVitaminb12() {
        return vitaminb12;
    }

    public void setVitaminb12(String vitaminb12) {
        this.vitaminb12 = vitaminb12;
    }

    public String getIodine() {
        return iodine;
    }

    public void setIodine(String iodine) {
        this.iodine = iodine;
    }

    public String getIron() {
        return iron;
    }

    public void setIron(String iron) {
        this.iron = iron;
    }

    public String getCalcium() {
        return calcium;
    }

    public void setCalcium(String calcium) {
        this.calcium = calcium;
    }

}